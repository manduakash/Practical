public class IfElseLadder{
	//main method
	public static void main(String[] args) {
		
	    String name = "Riya";
		
		//logic
		if(name=="Meena"){
			System.out.println("Name is Meena");
		}else if(name=="Neha"){
			System.out.println("Name is Neha");
		}else if(name=="Amit"){
			System.out.println("Name is Amit");
		}else{
			System.out.println("Name is "+ name);
		}
	
    }
}